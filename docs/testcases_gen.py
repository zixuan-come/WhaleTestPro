# ==================== 样式 & 输出 ====================
HEAD_FILL = PatternFill("solid", fgColor="2F5597")
HEAD_FONT = Font(bold=True, color="FFFFFF", size=11)
CELL_ALIGN = Alignment(vertical="top", wrap_text=True)
HEAD_ALIGN = Alignment(vertical="center", horizontal="center", wrap_text=True)
THIN = Side(style="thin", color="BFBFBF")
BORDER = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)
WIDTHS = [14, 16, 20, 30, 8, 26, 24, 40, 46]
PRIO_FILL = {"P0": "FFC7CE", "P1": "FFEB9C", "P2": "E2EFDA"}


def build_sheet(ws, rows):
    ws.append(COLUMNS)
    for c in ws[1]:
        c.fill, c.font, c.alignment, c.border = HEAD_FILL, HEAD_FONT, HEAD_ALIGN, BORDER
    for i, w in enumerate(WIDTHS, 1):
        ws.column_dimensions[chr(64 + i)].width = w
    for r in rows:
        ws.append(list(r))
        row = ws[ws.max_row]
        for cell in row:
            cell.alignment, cell.border = CELL_ALIGN, BORDER
        if row[4].value in PRIO_FILL:
            row[4].fill = PatternFill("solid", fgColor=PRIO_FILL[row[4].value])
    ws.freeze_panes = "A2"
    ws.auto_filter.ref = f"A1:I{ws.max_row}"


def build_book(modules, filename):
    wb = Workbook()
    wb.remove(wb.active)
    for name, rows in modules:
        build_sheet(wb.create_sheet(name), rows)
    out = os.path.join(os.path.dirname(os.path.abspath(__file__)), filename)
    wb.save(out)
    total = sum(len(r) for _, r in modules)
    print(f"OK: {out} | sheets={len(modules)} cases={total}")


def main():
    build_book(API_MODULES, "testcases_api.xlsx")
    build_book(FUNC_MODULES, "testcases_func.xlsx")


if __name__ == "__main__":
    main()
